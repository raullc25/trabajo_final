package prueba;

public class Pez {

	 	private String nombre;
	    private double peso;

	    public Pez(String nombre, double peso) {
	        this.nombre = nombre;
	        this.peso = peso;
	    }

	    public String getNombre() {
	        return nombre;
	    }

	    public double getPeso() {
	        return peso;
	    }
}
	

